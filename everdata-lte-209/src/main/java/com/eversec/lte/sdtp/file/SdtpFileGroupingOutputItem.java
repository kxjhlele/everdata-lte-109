package com.eversec.lte.sdtp.file;

public class SdtpFileGroupingOutputItem extends FileOutputItem {

	protected short Interface;
	protected int apptype;
	protected String city;
	protected String imsi;

	public SdtpFileGroupingOutputItem(String line, long time, short Interface,
			int apptype, String city, String imsi) {
		super(line, time);
		this.Interface = Interface;
		this.apptype = apptype;
		this.city = city;
		this.imsi = imsi;
	}

}
