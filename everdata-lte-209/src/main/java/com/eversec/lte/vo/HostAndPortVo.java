package com.eversec.lte.vo;

import java.io.Serializable;

public class HostAndPortVo implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = -2242552539228017894L;
	String host;
	int port;

	public HostAndPortVo() {
		super();
	}

	public HostAndPortVo(String host, int port) {
		super();
		this.host = host;
		this.port = port;
	}
	
	public String getHost() {
		return host;
	}
	public void setHost(String host) {
		this.host = host;
	}
	public int getPort() {
		return port;
	}
	public void setPort(int port) {
		this.port = port;
	}
	
}
