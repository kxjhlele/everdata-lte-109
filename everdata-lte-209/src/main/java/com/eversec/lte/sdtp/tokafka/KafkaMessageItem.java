package com.eversec.lte.sdtp.tokafka;

public class KafkaMessageItem {
	String topic;
	String key;
	byte[] bytes;

	public KafkaMessageItem(String topic, String key, byte[] bytes) {
		super();
		this.topic = topic;
		this.key = key;
		this.bytes = bytes;
	}

	public String getTopic() {
		return topic;
	}

	public String getKey() {
		return key;
	}

	public byte[] getBytes() {
		return bytes;
	}

}
